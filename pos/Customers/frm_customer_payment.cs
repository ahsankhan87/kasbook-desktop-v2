        private void btn_save_Click(object sender, EventArgs e)
        {
            try
            {
                if (string.IsNullOrWhiteSpace(_invoice_no) || _customer_id == 0)
                {
                    UiMessages.ShowInfo(
                        "Please select a customer and enter the payment details.",
                        "يرجى اختيار عميل وإدخال بيانات الدفعة.",
                        "Validation",
                        "التحقق"
                    );
                    return;
                }

                if (string.IsNullOrWhiteSpace(txt_total_amount.Text))
                {
                    UiMessages.ShowInfo(
                        "Payment amount is required.",
                        "مبلغ الدفعة مطلوب.",
                        "Validation",
                        "التحقق"
                    );
                    return;
                }

                double amount;
                if (!double.TryParse(txt_total_amount.Text, out amount) || amount <= 0)
                {
                    UiMessages.ShowInfo(
                        "Please enter a valid payment amount.",
                        "يرجى إدخال مبلغ دفعة صحيح.",
                        "Validation",
                        "التحقق"
                    );
                    return;
                }

                double discountAmount = 0;
                if (!string.IsNullOrWhiteSpace(txt_discount.Text) && !double.TryParse(txt_discount.Text, out discountAmount))
                {
                    UiMessages.ShowInfo(
                        "Please enter a valid discount amount.",
                        "يرجى إدخال مبلغ خصم صحيح.",
                        "Validation",
                        "التحقق"
                    );
                    return;
                }

                string appliedInvoiceNo = GetAppliedInvoiceNo();
                double appliedInvoiceBalance = GetAppliedInvoiceBalance();
                if (!string.IsNullOrWhiteSpace(appliedInvoiceNo) && (amount + discountAmount) > appliedInvoiceBalance + 0.0001)
                {
                    UiMessages.ShowWarning(
                        "Payment plus discount cannot be greater than the selected invoice balance.",
                        "لا يمكن أن يكون مبلغ الدفعة مع الخصم أكبر من رصيد الفاتورة المحددة.",
                        "Validation",
                        "التحقق"
                    );
                    return;
                }

