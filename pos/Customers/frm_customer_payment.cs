                            // Insert into reference account (debit) in bank payment
                            Insert_Journal_entry(_invoice_no, GL_account_id, amount, 0, txt_payment_date.Value.Date, journalDescription, 0, 0, entry_id, Ref_account_id);
                        }
                    }

