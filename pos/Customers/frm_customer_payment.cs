                if (string.IsNullOrWhiteSpace(txt_total_amount.Text) || !double.TryParse(txt_total_amount.Text, out existingAmount) || existingAmount <= 0)
                    txt_total_amount.Text = Math.Round(balance, 2).ToString("N2");
            }

            UpdateSuggestedDescription();
        }

