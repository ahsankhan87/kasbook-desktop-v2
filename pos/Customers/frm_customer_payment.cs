        private string GetAppliedInvoiceNo()
        {
            return cmb_pendingInvoices == null || cmb_pendingInvoices.SelectedValue == null
                ? string.Empty
                : Convert.ToString(cmb_pendingInvoices.SelectedValue);
        }

        private double GetAppliedInvoiceBalance()
        {
            if (cmb_pendingInvoices == null || cmb_pendingInvoices.SelectedItem == null)
                return 0;

            DataRowView rowView = cmb_pendingInvoices.SelectedItem as DataRowView;
            if (rowView == null || rowView.Row == null || !rowView.Row.Table.Columns.Contains("balance_amount"))
                return 0;

            double balance;
            return double.TryParse(Convert.ToString(rowView["balance_amount"]), out balance) ? balance : 0;
        }

        private static string BuildLedgerDescription(string paymentReferenceInvoiceNo, string appliedInvoiceNo, string description)
        {
            string baseDescription = (description ?? string.Empty).Trim();
            string againstInvoice = string.IsNullOrWhiteSpace(appliedInvoiceNo) ? string.Empty : "Against Invoice: " + appliedInvoiceNo;
            string paymentReference = PaymentReferencePrefix + paymentReferenceInvoiceNo + "]";

            if (string.IsNullOrWhiteSpace(baseDescription))
                return string.IsNullOrWhiteSpace(againstInvoice) ? paymentReference : againstInvoice + " " + paymentReference;

            if (string.IsNullOrWhiteSpace(againstInvoice))
                return baseDescription + " " + paymentReference;

            return againstInvoice + " | " + baseDescription + " " + paymentReference;
        }

        private void UpdateSuggestedDescription()
        {
            if (txt_description == null)
                return;

            string suggested = BuildPaymentDescription(
                GetAppliedInvoiceNo(),
                string.Empty,
                IsSelectedBankPayment(),
                GetSelectedReferenceDisplayText(),
                false,
                _invoice_no,
                false);

            string currentText = (txt_description.Text ?? string.Empty).Trim();
            if (string.IsNullOrWhiteSpace(currentText) || string.Equals(currentText, _autoGeneratedDescription, StringComparison.OrdinalIgnoreCase))
                txt_description.Text = suggested;

            _autoGeneratedDescription = suggested;
        }

        private string GetSelectedReferenceDisplayText()
        {
            if (cmb_ref_account_code == null)
                return string.Empty;

            DataRowView rowView = cmb_ref_account_code.SelectedItem as DataRowView;
            if (rowView != null)
            {
                if (rowView.Row.Table.Columns.Contains("display_name"))
                    return Convert.ToString(rowView["display_name"]);

                if (rowView.Row.Table.Columns.Contains("name"))
                    return Convert.ToString(rowView["name"]);
            }

            return Convert.ToString(cmb_ref_account_code.Text);
        }

        private string GetSelectedGlAccountDisplayText()
        {
            return cmb_GL_account_code == null ? string.Empty : Convert.ToString(cmb_GL_account_code.Text);
        }

        private bool IsSelectedBankPayment()
        {
            int glAccountId;
            if (cmb_GL_account_code == null || cmb_GL_account_code.SelectedValue == null || !int.TryParse(Convert.ToString(cmb_GL_account_code.SelectedValue), out glAccountId))
                return false;

            return new BankBLL().IsBankGlAccount(glAccountId);
        }

        private string GetEffectiveRemarks()
        {
            string currentText = (txt_description.Text ?? string.Empty).Trim();
            if (string.IsNullOrWhiteSpace(currentText))
                return string.Empty;

            return string.Equals(currentText, _autoGeneratedDescription, StringComparison.OrdinalIgnoreCase)
                ? string.Empty
                : currentText;
        }

        private string BuildPaymentDescription(string appliedInvoiceNo, string remarks, bool isBankPayment, string bankName, bool includePaymentReference, string paymentReferenceInvoiceNo, bool isDiscount)
        {
            bool isArabic = IsArabicEnvironment();

            string invoicePart = string.IsNullOrWhiteSpace(appliedInvoiceNo)
                ? (isArabic ? "على حساب العميل" : "on customer account")
                : (isArabic ? "مقابل الفاتورة " + appliedInvoiceNo : "against invoice " + appliedInvoiceNo);

            string paymentMode = isBankPayment
                ? (string.IsNullOrWhiteSpace(bankName)
                    ? (isArabic ? "البنك" : "Bank")
                    : (isArabic ? "البنك: " + bankName : "Bank: " + bankName))
                : GetSelectedGlAccountDisplayText();

            if (string.IsNullOrWhiteSpace(paymentMode))
                paymentMode = isBankPayment
                    ? (isArabic ? "البنك" : "Bank")
                    : (isArabic ? "النقد" : "Cash");

            string description = isDiscount
                ? (isArabic
                    ? "تم ترحيل خصم دفعة العميل " + invoicePart + " عبر " + paymentMode + "."
                    : "Customer payment discount posted " + invoicePart + " via " + paymentMode + ".")
                : (isArabic
                    ? "تم استلام دفعة العميل " + invoicePart + " عبر " + paymentMode + "."
                    : "Customer payment received " + invoicePart + " via " + paymentMode + ".");

            if (!string.IsNullOrWhiteSpace(remarks))
                description += isArabic
                    ? " ملاحظات: " + remarks.Trim() + "."
                    : " Remarks: " + remarks.Trim() + ".";

            if (includePaymentReference && !string.IsNullOrWhiteSpace(paymentReferenceInvoiceNo))
                description += " " + PaymentReferencePrefix + paymentReferenceInvoiceNo + "]";

            return description.Trim();
        }

        private static bool IsArabicEnvironment()
        {
            string lang = UsersModal.logged_in_lang ?? string.Empty;
            return lang.StartsWith("ar", StringComparison.OrdinalIgnoreCase);
        }
