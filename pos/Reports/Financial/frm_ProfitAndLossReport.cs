        private void GenerateReport()
        {
            if (dtpFrom.Value.Date > dtpTo.Value.Date)
            {
                UiMessages.ShowError("From date cannot be after To date.", "لا يمكن أن يكون تاريخ البداية بعد تاريخ النهاية.");
                return;
            }

            try
            {
                using (BusyScope.Show(this, UiMessages.T("Generating income statement...", "جاري إنشاء قائمة الدخل...")))
                {
                    _activeFrom = dtpFrom.Value.Date;
                    _activeTo = dtpTo.Value.Date;

                    if (chkShowComparison.Checked)
                    {
                        Tuple<DateTime, DateTime> previous = ResolvePreviousPeriod(_activeFrom, _activeTo);
                        _previousFrom = previous.Item1;
                        _previousTo = previous.Item2;
                    }
                    else
                    {
                        _previousFrom = null;
                        _previousTo = null;
                    }

                    DataTable hierarchyTable = _accountsBll.GetProfitLossHierarchy();
                    int? costCenterId = cmbCostCenter.SelectedValue == null || cmbCostCenter.SelectedValue == DBNull.Value
                        ? (int?)null
                        : Convert.ToInt32(cmbCostCenter.SelectedValue);

                    DataTable balancesTable = _accountsBll.GetProfitLossBalances(_activeFrom, _activeTo, _previousFrom, _previousTo, costCenterId);

                    BuildReportTree(hierarchyTable, balancesTable, IsDetailedView());
                    BindTree();
                    UpdateHeader();
                }
            }
            catch (Exception ex)
            {
                UiMessages.ShowError(ex.Message, ex.Message);
            }
        }

        private static Tuple<DateTime, DateTime> ResolvePreviousPeriod(DateTime from, DateTime to)
        {
            int days = (to - from).Days + 1;
            DateTime previousTo = from.AddDays(-1);
            DateTime previousFrom = previousTo.AddDays(-(days - 1));
            return Tuple.Create(previousFrom, previousTo);
        }

        private bool IsDetailedView()
        {
            string text = Convert.ToString(cmbDetailLevel.SelectedItem);
            return !string.IsNullOrWhiteSpace(text) && text.StartsWith("Detailed", StringComparison.OrdinalIgnoreCase);
        }

        private void BuildReportTree(DataTable hierarchyTable, DataTable balancesTable, bool detailed)
        {
            _reportLines.Clear();

            Dictionary<int, BalanceInfo> balances = ParseBalances(balancesTable);
            Dictionary<int, GroupInfo> groups;
            List<AccountInfo> accounts;
            ParseHierarchy(hierarchyTable, balances, out groups, out accounts);

            List<ReportLine> incomeLines = BuildSectionLines("Income", groups, accounts, detailed);
            List<ReportLine> cogsLines = BuildSectionLines("COGS", groups, accounts, detailed);
            List<ReportLine> expenseLines = BuildSectionLines("Expense", groups, accounts, detailed);
            List<ReportLine> otherLines = BuildSectionLines("Other", groups, accounts, detailed);

            decimal totalIncome = incomeLines.Sum(x => x.CurrentAmount);
            decimal totalCogs = cogsLines.Sum(x => x.CurrentAmount);
            decimal grossProfit = totalIncome - totalCogs;
            decimal grossMarginPercent = totalIncome == 0m ? 0m : (grossProfit / totalIncome) * 100m;
            decimal totalExpense = expenseLines.Sum(x => x.CurrentAmount);
            decimal operatingProfit = grossProfit - totalExpense;
            decimal otherResult = otherLines.Sum(x => x.CurrentAmount);
            decimal netProfit = operatingProfit + otherResult;

            _totalIncome = totalIncome;

            _reportLines.Add(ReportLine.SectionHeader("INCOME SECTION"));
            _reportLines.AddRange(incomeLines);
            _reportLines.Add(ReportLine.Subtotal("Total Income", totalIncome));

            _reportLines.Add(ReportLine.SectionHeader("COST OF GOODS SOLD"));
            _reportLines.AddRange(cogsLines);
            _reportLines.Add(ReportLine.Highlight("Gross Profit", grossProfit));
            _reportLines.Add(ReportLine.MutedMetric("Gross Profit Margin %", grossMarginPercent));

            _reportLines.Add(ReportLine.SectionHeader("OPERATING EXPENSES"));
            _reportLines.AddRange(expenseLines);
            _reportLines.Add(ReportLine.Subtotal("Total Operating Expenses", totalExpense));
            _reportLines.Add(ReportLine.Highlight("Operating Profit / (Loss)", operatingProfit));

            if (otherLines.Count > 0)
            {
                _reportLines.Add(ReportLine.SectionHeader("OTHER INCOME / EXPENSES"));
                _reportLines.AddRange(otherLines);
            }

            _reportLines.Add(ReportLine.NetResult("Net Profit / (Loss)", netProfit));

            foreach (ReportLine line in _reportLines)
            {
                ComputePercentRecursive(line, totalIncome);
            }
        }

        private static Dictionary<int, BalanceInfo> ParseBalances(DataTable balancesTable)
        {
            var map = new Dictionary<int, BalanceInfo>();
            foreach (DataRow row in balancesTable.Rows)
            {
                int accountId = row["account_id"] == DBNull.Value ? 0 : Convert.ToInt32(row["account_id"]);
                if (accountId <= 0)
                {
                    continue;
                }

                map[accountId] = new BalanceInfo
                {
                    CurrentDebit = row["current_debit"] == DBNull.Value ? 0m : Convert.ToDecimal(row["current_debit"]),
                    CurrentCredit = row["current_credit"] == DBNull.Value ? 0m : Convert.ToDecimal(row["current_credit"]),
                    PreviousDebit = row["previous_debit"] == DBNull.Value ? 0m : Convert.ToDecimal(row["previous_debit"]),
                    PreviousCredit = row["previous_credit"] == DBNull.Value ? 0m : Convert.ToDecimal(row["previous_credit"])
                };
            }

            return map;
        }

        private static void ParseHierarchy(DataTable hierarchyTable, Dictionary<int, BalanceInfo> balances, out Dictionary<int, GroupInfo> groups, out List<AccountInfo> accounts)
        {
            groups = new Dictionary<int, GroupInfo>();
            accounts = new List<AccountInfo>();

            foreach (DataRow row in hierarchyTable.Rows)
            {
                string kind = Convert.ToString(row["node_kind"]);
                if (string.Equals(kind, "Group", StringComparison.OrdinalIgnoreCase))
                {
                    int groupId = row["group_id"] == DBNull.Value ? 0 : Convert.ToInt32(row["group_id"]);
                    if (groupId <= 0)
                    {
                        continue;
                    }

                    groups[groupId] = new GroupInfo
                    {
                        Id = groupId,
                        ParentId = row["parent_id"] == DBNull.Value ? 0 : Convert.ToInt32(row["parent_id"]),
                        Code = Convert.ToString(row["code"]),
                        Name = Convert.ToString(row["name"]),
                        Section = Convert.ToString(row["section"])
                    };
                }
                else if (string.Equals(kind, "Account", StringComparison.OrdinalIgnoreCase))
                {
                    int accountId = row["account_id"] == DBNull.Value ? 0 : Convert.ToInt32(row["account_id"]);
                    if (accountId <= 0)
                    {
                        continue;
                    }

                    BalanceInfo balance;
                    if (!balances.TryGetValue(accountId, out balance))
                    {
                        balance = new BalanceInfo();
                    }

                    string section = Convert.ToString(row["section"]);
                    accounts.Add(new AccountInfo
                    {
                        Id = accountId,
                        GroupId = row["group_id"] == DBNull.Value ? 0 : Convert.ToInt32(row["group_id"]),
                        Code = Convert.ToString(row["code"]),
                        Name = Convert.ToString(row["name"]),
                        Section = section,
                        CurrentAmount = ToProfitLossAmount(section, balance.CurrentDebit, balance.CurrentCredit),
                        PreviousAmount = ToProfitLossAmount(section, balance.PreviousDebit, balance.PreviousCredit)
                    });
                }
            }
        }

        private static decimal ToProfitLossAmount(string section, decimal debit, decimal credit)
        {
            if (string.Equals(section, "Income", StringComparison.OrdinalIgnoreCase))
            {
                return credit - debit;
            }

            return debit - credit;
        }

        private static List<ReportLine> BuildSectionLines(string section, Dictionary<int, GroupInfo> groups, List<AccountInfo> accounts, bool detailed)
        {
            var sectionGroups = groups.Values.Where(x => string.Equals(x.Section, section, StringComparison.OrdinalIgnoreCase)).ToDictionary(x => x.Id, x => x);
            var groupChildren = sectionGroups.Values.GroupBy(x => x.ParentId).ToDictionary(g => g.Key, g => g.OrderBy(x => x.Code).ThenBy(x => x.Name).ToList());
            var accountsByGroup = accounts.Where(x => string.Equals(x.Section, section, StringComparison.OrdinalIgnoreCase)).GroupBy(x => x.GroupId).ToDictionary(g => g.Key, g => g.OrderBy(x => x.Code).ThenBy(x => x.Name).ToList());

            var roots = sectionGroups.Values.Where(g => g.ParentId == 0 || !sectionGroups.ContainsKey(g.ParentId)).OrderBy(x => x.Code).ThenBy(x => x.Name).ToList();
            var lines = new List<ReportLine>();
            foreach (GroupInfo root in roots)
            {
                ReportLine line = BuildGroupLineRecursive(root, groupChildren, accountsByGroup, detailed, 1);
                if (line != null)
                {
                    lines.Add(line);
                }
            }

            return lines;
        }

        private static ReportLine BuildGroupLineRecursive(GroupInfo group, Dictionary<int, List<GroupInfo>> groupChildren, Dictionary<int, List<AccountInfo>> accountsByGroup, bool detailed, int level)
        {
            var line = new ReportLine
            {
                Name = string.IsNullOrWhiteSpace(group.Name) ? "(Group)" : group.Name,
                LineType = ReportLineType.Group,
                Level = level,
                Children = new List<ReportLine>()
            };

            List<GroupInfo> children;
            if (groupChildren.TryGetValue(group.Id, out children))
            {
                foreach (GroupInfo child in children)
                {
                    ReportLine childLine = BuildGroupLineRecursive(child, groupChildren, accountsByGroup, detailed, level + 1);
                    if (childLine != null)
                    {
                        line.CurrentAmount += childLine.CurrentAmount;
                        line.PreviousAmount += childLine.PreviousAmount;

                        if (detailed || level <= 1)
                        {
                            line.Children.Add(childLine);
                        }
                    }
                }
            }

            List<AccountInfo> accountList;
            if (accountsByGroup.TryGetValue(group.Id, out accountList))
            {
                foreach (AccountInfo account in accountList)
                {
                    line.CurrentAmount += account.CurrentAmount;
                    line.PreviousAmount += account.PreviousAmount;

                    if (detailed)
                    {
                        line.Children.Add(new ReportLine
                        {
                            Name = string.IsNullOrWhiteSpace(account.Code) ? account.Name : account.Code + " - " + account.Name,
                            LineType = ReportLineType.Account,
                            AccountId = account.Id,
                            Level = level + 1,
                            CurrentAmount = account.CurrentAmount,
                            PreviousAmount = account.PreviousAmount,
                            IsClickable = true
                        });
                    }
                }
            }

            if (line.Children.Count == 0 && line.CurrentAmount == 0m && line.PreviousAmount == 0m)
            {
                return null;
            }

            return line;
        }

        private static void ComputePercentRecursive(ReportLine line, decimal totalIncome)
        {
            if (Math.Abs(totalIncome) > 0.0001m)
            {
                line.PercentOfIncome = (line.CurrentAmount / totalIncome) * 100m;
            }

            if (line.Children == null)
            {
                return;
            }

            foreach (ReportLine child in line.Children)
            {
                ComputePercentRecursive(child, totalIncome);
            }
        }

        private void BindTree()
        {
            treeReport.BeginUpdate();
            try
            {
                treeReport.Nodes.Clear();

                foreach (ReportLine line in _reportLines)
                {
                    AddTreeNode(treeReport.Nodes, line);
                }

                treeReport.ExpandAll();
            }
            finally
            {
                treeReport.EndUpdate();
            }
        }

        private static void AddTreeNode(TreeNodeCollection nodes, ReportLine line)
        {
            var node = new TreeNode(line.Name) { Tag = line };
            nodes.Add(node);

            if (line.Children == null)
            {
                return;
            }

            foreach (ReportLine child in line.Children)
            {
                AddTreeNode(node.Nodes, child);
            }
        }

