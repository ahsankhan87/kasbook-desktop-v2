        private void TreeReport_DrawNode(object sender, DrawTreeNodeEventArgs e)
        {
            e.DrawDefault = false;

            Graphics g = e.Graphics;
            ReportLine line = e.Node.Tag as ReportLine;
            if (line == null)
            {
                e.DrawDefault = true;
                return;
            }

            var state = g.Save();
            try
            {
                g.SetClip(new Rectangle(0, e.Bounds.Top, treeReport.ClientSize.Width, e.Bounds.Height));

                Color backColor = (e.State & TreeNodeStates.Selected) == TreeNodeStates.Selected ? Color.FromArgb(224, 236, 255) : treeReport.BackColor;
                using (var backBrush = new SolidBrush(backColor))
                {
                    g.FillRectangle(backBrush, new Rectangle(0, e.Bounds.Top, treeReport.ClientSize.Width, e.Bounds.Height));
                }

                Font font = treeReport.Font;
                Color foreColor = AppTheme.TextPrimary;

                if (line.LineType == ReportLineType.SectionHeader)
                {
                    font = new Font(treeReport.Font, FontStyle.Bold);
                    foreColor = Color.FromArgb(27, 94, 32);
                }
                else if (line.LineType == ReportLineType.Subtotal || line.LineType == ReportLineType.Highlight)
                {
                    font = new Font(treeReport.Font, FontStyle.Bold);
                    foreColor = Color.FromArgb(13, 71, 161);
                }
                else if (line.LineType == ReportLineType.NetResult)
                {
                    font = new Font(treeReport.Font.FontFamily, treeReport.Font.Size + 1.5f, FontStyle.Bold);
                    foreColor = line.CurrentAmount >= 0 ? Color.ForestGreen : Color.Firebrick;
                }
                else if (line.LineType == ReportLineType.MutedMetric)
                {
                    foreColor = Color.DimGray;
                }
                else if (line.IsClickable)
                {
                    font = new Font(treeReport.Font, FontStyle.Underline);
                    foreColor = Color.FromArgb(25, 118, 210);
                }

                int clientWidth = treeReport.ClientSize.Width;
                int amountX = clientWidth - 430;
                int percentX = clientWidth - 300;
                int previousX = clientWidth - 200;
                int varianceX = clientWidth - 95;
                int textTop = e.Bounds.Top + 2;
                int textHeight = e.Bounds.Height - 4;
                int nameWidth = Math.Max(120, amountX - e.Bounds.Left - 12);

                TextRenderer.DrawText(
                    g,
                    line.Name,
                    font,
                    new Rectangle(e.Bounds.Left, textTop, nameWidth, textHeight),
                    foreColor,
                    TextFormatFlags.Left | TextFormatFlags.VerticalCenter | TextFormatFlags.EndEllipsis | TextFormatFlags.NoPrefix);

                TextRenderer.DrawText(
                    g,
                    FormatAmount(line.CurrentAmount, line.LineType == ReportLineType.MutedMetric),
                    font,
                    new Rectangle(amountX, textTop, 100, textHeight),
                    foreColor,
                    TextFormatFlags.Right | TextFormatFlags.VerticalCenter | TextFormatFlags.NoPadding | TextFormatFlags.NoPrefix);

                if (chkShowPercentage.Checked)
                {
                    TextRenderer.DrawText(
                        g,
                        line.PercentOfIncome.ToString("N2", CultureInfo.InvariantCulture) + "%",
                        treeReport.Font,
                        new Rectangle(percentX, textTop, 90, textHeight),
                        Color.DimGray,
                        TextFormatFlags.Right | TextFormatFlags.VerticalCenter | TextFormatFlags.NoPadding | TextFormatFlags.NoPrefix);
                }

                if (chkShowComparison.Checked)
                {
                    TextRenderer.DrawText(
                        g,
                        line.PreviousAmount.ToString("N2", CultureInfo.InvariantCulture),
                        treeReport.Font,
                        new Rectangle(previousX, textTop, 90, textHeight),
                        Color.Gray,
                        TextFormatFlags.Right | TextFormatFlags.VerticalCenter | TextFormatFlags.NoPadding | TextFormatFlags.NoPrefix);
                    string varianceText = BuildVarianceText(line.CurrentAmount, line.PreviousAmount);
                    Color varianceColor = line.CurrentAmount >= line.PreviousAmount ? Color.ForestGreen : Color.Firebrick;
                    TextRenderer.DrawText(
                        g,
                        varianceText,
                        treeReport.Font,
                        new Rectangle(varianceX, textTop, 90, textHeight),
                        varianceColor,
                        TextFormatFlags.Right | TextFormatFlags.VerticalCenter | TextFormatFlags.NoPadding | TextFormatFlags.NoPrefix);
                }
            }
            finally
            {
                g.Restore(state);
            }
        }

        private static string FormatAmount(decimal amount, bool isPercentLine)
        {
            if (isPercentLine)
            {
                return amount.ToString("N2", CultureInfo.InvariantCulture) + "%";
            }

            return amount.ToString("N2", CultureInfo.InvariantCulture);
        }

        private static string BuildVarianceText(decimal current, decimal previous)
        {
            if (previous == 0m)
            {
                return current == 0m ? "→ 0.00%" : "▲ 100.00%";
            }

            decimal change = ((current - previous) / Math.Abs(previous)) * 100m;
            string arrow = change >= 0m ? "▲" : "▼";
            return string.Format(CultureInfo.InvariantCulture, "{0} {1:N2}%", arrow, Math.Abs(change));
        }

