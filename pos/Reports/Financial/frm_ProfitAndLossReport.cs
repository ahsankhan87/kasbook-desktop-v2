        private bool ExportPdfToPath(string path, bool silent)
        {
            try
            {
                PrintDocument document = CreatePrintDocument();
                document.PrinterSettings.PrinterName = "Microsoft Print to PDF";

                if (!document.PrinterSettings.IsValid)
                {
                    if (!silent)
                    {
                        UiMessages.ShowWarning("Microsoft Print to PDF printer is not available.", "طابعة Microsoft Print to PDF غير متوفرة.");
                    }
                    return false;
                }

                document.PrinterSettings.PrintToFile = true;
                document.PrinterSettings.PrintFileName = path;
                document.DefaultPageSettings.Landscape = false;
                document.PrintController = new StandardPrintController();
                document.Print();
                return File.Exists(path);
            }
            catch (Exception ex)
            {
                if (!silent)
                {
                    UiMessages.ShowError(ex.Message, ex.Message);
                }
                return false;
            }
        }

        private PrintDocument CreatePrintDocument()
        {
            List<PrintRow> rows = BuildPrintRows();
            int currentIndex = 0;

            PrintDocument document = new PrintDocument();
            document.DocumentName = "Profit & Loss";
            document.DefaultPageSettings.PaperSize = new PaperSize("A4", 827, 1169);

            document.PrintPage += (s, e) =>
            {
                int left = e.MarginBounds.Left;
                int y = e.MarginBounds.Top;
                int width = e.MarginBounds.Width;

                using (Font titleFont = new Font("Segoe UI", 12F, FontStyle.Bold))
                using (Font headFont = new Font("Segoe UI", 8F, FontStyle.Bold))
                using (Font bodyFont = new Font("Segoe UI", 8F))
                using (Pen linePen = new Pen(Color.Silver))
                {
                    e.Graphics.DrawString(lblCompanyName.Text, titleFont, Brushes.Black, left, y);
                    y += 22;
                    e.Graphics.DrawString("Income Statement (Profit & Loss)", titleFont, Brushes.Black, left, y);
                    y += 20;
                    e.Graphics.DrawString(lblReportPeriod.Text, bodyFont, Brushes.DimGray, left, y);
                    y += 18;

                    int colName = left;
                    int colAmt = left + width - 260;
                    int colPct = left + width - 160;
                    int colPrev = left + width - 80;

                    e.Graphics.DrawString("Account", headFont, Brushes.Black, colName, y);
                    e.Graphics.DrawString("Amount", headFont, Brushes.Black, colAmt, y);
                    e.Graphics.DrawString("% Income", headFont, Brushes.Black, colPct, y);
                    if (chkShowComparison.Checked)
                    {
                        e.Graphics.DrawString("Prev", headFont, Brushes.Black, colPrev, y);
                    }

                    y += 16;
                    e.Graphics.DrawLine(linePen, left, y, left + width, y);
                    y += 4;

                    while (currentIndex < rows.Count)
                    {
                        if (y + 16 > e.MarginBounds.Bottom)
                        {
                            e.HasMorePages = true;
                            return;
                        }

                        PrintRow row = rows[currentIndex];
                        Font rowFont = row.Bold ? headFont : bodyFont;
                        Brush rowBrush = row.ForeColor.HasValue ? new SolidBrush(row.ForeColor.Value) : Brushes.Black;

                        e.Graphics.DrawString(row.Name, rowFont, rowBrush, colName + (row.Level * 14), y);
                        e.Graphics.DrawString(row.AmountText, rowFont, rowBrush, colAmt, y);
                        if (chkShowPercentage.Checked)
                        {
                            e.Graphics.DrawString(row.PercentText, bodyFont, Brushes.DimGray, colPct, y);
                        }
                        if (chkShowComparison.Checked)
                        {
                            e.Graphics.DrawString(row.PreviousText, bodyFont, Brushes.Gray, colPrev, y);
                        }

                        if (!ReferenceEquals(rowBrush, Brushes.Black))
                        {
                            rowBrush.Dispose();
                        }

                        y += 16;
                        currentIndex++;
                    }
                }

                e.HasMorePages = false;
            };

            return document;
        }

        private List<PrintRow> BuildPrintRows()
        {
            var rows = new List<PrintRow>();
            foreach (ReportLine line in _reportLines)
            {
                AddPrintRowsRecursive(rows, line, 0);
            }

            return rows;
        }

        private static void AddPrintRowsRecursive(List<PrintRow> rows, ReportLine line, int depth)
        {
            rows.Add(new PrintRow
            {
                Name = line.Name,
                Level = depth,
                AmountText = line.LineType == ReportLineType.MutedMetric
                    ? line.CurrentAmount.ToString("N2", CultureInfo.InvariantCulture) + "%"
                    : line.CurrentAmount.ToString("N2", CultureInfo.InvariantCulture),
                PercentText = line.PercentOfIncome.ToString("N2", CultureInfo.InvariantCulture) + "%",
                PreviousText = line.PreviousAmount.ToString("N2", CultureInfo.InvariantCulture),
                Bold = line.LineType == ReportLineType.SectionHeader || line.LineType == ReportLineType.Subtotal || line.LineType == ReportLineType.Highlight || line.LineType == ReportLineType.NetResult,
                ForeColor = line.LineType == ReportLineType.NetResult
                    ? (line.CurrentAmount >= 0m ? (Color?)Color.ForestGreen : Color.Firebrick)
                    : (Color?)null
            });

            if (line.Children == null)
            {
                return;
            }

            foreach (ReportLine child in line.Children)
            {
                AddPrintRowsRecursive(rows, child, depth + 1);
            }
        }

        private List<ExportRow> BuildExportRows()
        {
            List<ExportRow> rows = new List<ExportRow>();
            int rowIndex = 2;
            foreach (ReportLine line in _reportLines)
            {
                AddExportRowsRecursive(rows, line, 0, ref rowIndex);
            }

            foreach (ExportRow row in rows.Where(x => x.ChildRowIndexes.Count > 0))
            {
                int min = row.ChildRowIndexes.Min();
                int max = row.ChildRowIndexes.Max();
                row.AmountFormula = string.Format(CultureInfo.InvariantCulture, "=SUM(B{0}:B{1})", min, max);
                row.PreviousFormula = string.Format(CultureInfo.InvariantCulture, "=SUM(C{0}:C{1})", min, max);
            }

            return rows;
        }

        private static void AddExportRowsRecursive(List<ExportRow> rows, ReportLine line, int depth, ref int rowIndex)
        {
            ExportRow row = new ExportRow
            {
                RowIndex = rowIndex,
                Name = new string(' ', depth * 2) + line.Name,
                AmountValue = line.CurrentAmount,
                PreviousValue = line.PreviousAmount,
                PercentValue = line.PercentOfIncome,
                ChildRowIndexes = new List<int>()
            };

            rows.Add(row);
            rowIndex++;

            if (line.Children == null)
            {
                return;
            }

            foreach (ReportLine child in line.Children)
            {
                int before = rowIndex;
                AddExportRowsRecursive(rows, child, depth + 1, ref rowIndex);
                for (int idx = before; idx < rowIndex; idx++)
                {
                    row.ChildRowIndexes.Add(idx);
                }
            }
        }

        private static void WriteCsvWithFormulas(string filePath, List<ExportRow> rows)
        {
            StringBuilder sb = new StringBuilder();
            sb.AppendLine("Line Item,Amount,Previous Period,Variance %,As % of Total Income");

            foreach (ExportRow row in rows)
            {
                string amountCell = string.IsNullOrWhiteSpace(row.AmountFormula)
                    ? row.AmountValue.ToString("N2", CultureInfo.InvariantCulture)
                    : row.AmountFormula;
                string previousCell = string.IsNullOrWhiteSpace(row.PreviousFormula)
                    ? row.PreviousValue.ToString("N2", CultureInfo.InvariantCulture)
                    : row.PreviousFormula;
                string varianceCell = string.Format(CultureInfo.InvariantCulture, "=IF(C{0}=0,0,(B{0}-C{0})/ABS(C{0}))", row.RowIndex);
                string percentCell = row.PercentValue.ToString("N2", CultureInfo.InvariantCulture) + "%";

                sb.Append(EscapeCsv(row.Name)).Append(',')
                    .Append(EscapeCsv(amountCell)).Append(',')
                    .Append(EscapeCsv(previousCell)).Append(',')
                    .Append(EscapeCsv(varianceCell)).Append(',')
                    .Append(EscapeCsv(percentCell)).AppendLine();
            }

            File.WriteAllText(filePath, sb.ToString(), Encoding.UTF8);
            Process.Start(filePath);
        }

        private static string EscapeCsv(string value)
        {
            string text = value ?? string.Empty;
            if (!text.Contains(",") && !text.Contains("\"") && !text.Contains("\n") && !text.Contains("\r"))
            {
                return text;
            }

            return "\"" + text.Replace("\"", "\"\"") + "\"";
        }

        private static bool OpenComposeWithAttachment(string attachmentPath)
        {
            MapiMessage message = new MapiMessage();
            MapiFileDesc file = new MapiFileDesc();
            file.nPosition = -1;
            file.lpszPathName = attachmentPath;
            file.lpszFileName = Path.GetFileName(attachmentPath);

            IntPtr filePtr = IntPtr.Zero;
            try
            {
                int fileSize = Marshal.SizeOf(typeof(MapiFileDesc));
                filePtr = Marshal.AllocHGlobal(fileSize);
                Marshal.StructureToPtr(file, filePtr, false);

                message.lpszSubject = "Income Statement (Profit & Loss)";
                message.lpszNoteText = "Please find the attached Profit & Loss report.";
                message.nFileCount = 1;
                message.lpFiles = filePtr;

                int result = MAPISendMail(IntPtr.Zero, IntPtr.Zero, message, MapiSendMailFlags.MAPI_DIALOG, 0);
                return result == 0 || result == 1;
            }
            finally
            {
                if (filePtr != IntPtr.Zero)
                {
                    Marshal.FreeHGlobal(filePtr);
                }
            }
        }

