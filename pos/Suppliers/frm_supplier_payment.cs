        private void btn_save_Click(object sender, EventArgs e)
        {
            try
            {
                if (string.IsNullOrWhiteSpace(_invoice_no) || _supplier_id == 0)
                {
                    UiMessages.ShowInfo("Please enter supplier payment details.", "يرجى إدخال بيانات دفعة المورد.", "Validation", "التحقق");
                    return;
                }

                double amount;
                if (!double.TryParse(txt_total_amount.Text, out amount) || amount <= 0)
                {
                    UiMessages.ShowInfo("Please enter a valid payment amount.", "يرجى إدخال مبلغ دفعة صحيح.", "Validation", "التحقق");
                    return;
                }

                double discountAmount = 0;
                if (!string.IsNullOrWhiteSpace(txt_discount.Text) && !double.TryParse(txt_discount.Text, out discountAmount))
                {
                    UiMessages.ShowInfo("Please enter a valid discount amount.", "يرجى إدخال مبلغ خصم صحيح.", "Validation", "التحقق");
                    return;
                }

                string appliedInvoiceNo = GetAppliedInvoiceNo();
                double appliedInvoiceBalance = GetAppliedInvoiceBalance();
                if (!string.IsNullOrWhiteSpace(appliedInvoiceNo) && (amount + discountAmount) > appliedInvoiceBalance + 0.0001)
                {
                    UiMessages.ShowWarning("Payment plus discount cannot be greater than the selected invoice balance.", "لا يمكن أن يكون مبلغ الدفعة مع الخصم أكبر من رصيد الفاتورة المحددة.", "Validation", "التحقق");
                    return;
                }

                var confirm = UiMessages.ConfirmYesNo("Post this supplier payment?", "هل تريد ترحيل دفعة المورد؟", captionEn: "Confirm", captionAr: "تأكيد");
                if (confirm != DialogResult.Yes)
                    return;

                using (BusyScope.Show(this, UiMessages.T("Posting payment...", "جاري ترحيل الدفعة...")))
                {
                    int glAccountId = (cmb_GL_account_code.SelectedValue == null ? 0 : int.Parse(cmb_GL_account_code.SelectedValue.ToString()));
                    int refAccountId = (cmb_ref_account_code.SelectedValue == null ? 0 : int.Parse(cmb_ref_account_code.SelectedValue.ToString()));

                    bool isBankPayment = new BankBLL().IsBankGlAccount(glAccountId);
                    string bankName = isBankPayment ? GetSelectedReferenceDisplayText() : string.Empty;
                    string userRemarks = GetEffectiveRemarks();

                    string ledgerInvoiceNo = string.IsNullOrWhiteSpace(appliedInvoiceNo) ? _invoice_no : appliedInvoiceNo;
                    string journalDescription = BuildPaymentDescription(appliedInvoiceNo, userRemarks, isBankPayment, bankName, false, _invoice_no, false);
                    string ledgerDescription = BuildPaymentDescription(appliedInvoiceNo, userRemarks, isBankPayment, bankName, true, _invoice_no, false);
                    string discountJournalDescription = BuildPaymentDescription(appliedInvoiceNo, userRemarks, isBankPayment, bankName, false, _invoice_no, true);
                    string discountLedgerDescription = BuildPaymentDescription(appliedInvoiceNo, userRemarks, isBankPayment, bankName, true, _invoice_no, true);

                    int entry_id = Insert_Journal_entry(_invoice_no, payable_account_id, amount, 0, txt_payment_date.Value.Date, journalDescription, 0, 0, 0, 0);
                    Insert_Journal_entry(_invoice_no, glAccountId, 0, amount, txt_payment_date.Value.Date, journalDescription, 0, 0, 0, 0);

                    if (refAccountId != 0 && isBankPayment)
                    {
                        Insert_Journal_entry(_invoice_no, glAccountId, 0, amount, txt_payment_date.Value.Date, journalDescription, 0, 0, entry_id, refAccountId);
                    }

                    Insert_Journal_entry(ledgerInvoiceNo, glAccountId, amount, 0, txt_payment_date.Value.Date, ledgerDescription, 0, _supplier_id, entry_id, 0);

                    if (!string.IsNullOrWhiteSpace(txt_discount.Text))
                    {
                        double discount;
                        if (double.TryParse(txt_discount.Text, out discount) && discount > 0)
                        {
                            Insert_Journal_entry(_invoice_no, payable_account_id, discount, 0, txt_payment_date.Value.Date, discountJournalDescription, 0, 0, 0, 0);
                            int entry_idd = Insert_Journal_entry(_invoice_no, purchases_discount_acc_id, 0, discount, txt_payment_date.Value.Date, discountJournalDescription, 0, 0, 0, 0);
                            Insert_Journal_entry(ledgerInvoiceNo, purchases_discount_acc_id, discount, 0, txt_payment_date.Value.Date, discountLedgerDescription, 0, _supplier_id, entry_idd, 0);
                        }
                    }

                    UiMessages.ShowInfo(
                        string.IsNullOrWhiteSpace(appliedInvoiceNo) ? "Supplier payment has been posted successfully." : "Supplier payment has been posted successfully against invoice " + appliedInvoiceNo + ".",
                        string.IsNullOrWhiteSpace(appliedInvoiceNo) ? "تم ترحيل دفعة المورد بنجاح." : "تم ترحيل دفعة المورد بنجاح مقابل الفاتورة " + appliedInvoiceNo + ".",
                        "Success",
                        "نجاح");

                    if (mainForm != null)
                        mainForm.load_transactions_grid(_supplier_id);

                    this.Close();
                }
            }
            catch (Exception ex)
            {
                UiMessages.ShowError(ex.Message, ex.Message);
            }
        }

        private int Insert_Journal_entry(string invoice_no, int account_id, double debit, double credit, DateTime date,
           string description, int customer_id, int supplier_id, int entry_id, int bank_id)
        {
            int journal_id = 0;
            JournalsModal JournalsModal_obj = new JournalsModal();
            JournalsBLL JournalsObj = new JournalsBLL();

            JournalsModal_obj.invoice_no = invoice_no;
            JournalsModal_obj.entry_date = date;
            JournalsModal_obj.debit = debit;
            JournalsModal_obj.credit = credit;
            JournalsModal_obj.account_id = account_id;
            JournalsModal_obj.description = description;
            JournalsModal_obj.customer_id = customer_id;
            JournalsModal_obj.supplier_id = supplier_id;
            JournalsModal_obj.entry_id = entry_id;
            JournalsModal_obj.bank_id = bank_id;

            journal_id = JournalsObj.Insert(JournalsModal_obj);
            return journal_id;
        }

